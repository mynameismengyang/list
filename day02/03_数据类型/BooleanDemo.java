//boolean数据类型
class BooleanDemo
{
	public static void main(String[] arga)
	{
		//boolean类型的常量:true,false
		System.out.println(true);
		System.out.println(false);

		//boolean类型的变量
		//数据类型 变量名 = 该类型的常量值;
		boolean isMan;
		isMan = true;
		System.out.println(isMan);

		isMan = false;//给isMan重新赋值
		System.out.println(isMan);

	}
}